// classlist

const h1 = document.querySelector("h1");

console.log(h1.classList);

// add() this adds the classes to the html element
h1.classList.add("styles");

// remove() this remove the classes to the html element

h1.classList.remove("styles");

// toggle() this does the opposite of what is the current state of the classes to the html element

h1.classList.toggle("styles");

console.log(h1.classList.toggle);
